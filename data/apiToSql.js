function apiToInsert(dataJson){
    dataJson.results.map((data) => {
        var name = formatLabel(data.name);
        var rating = data.rating ? data.rating : -1;
        var photoReference = 'NULL';
        if(data.photos){
            if(data.photos[0].photo_reference){
                photoReference = data.photos[0].photo_reference;
            }
        }
        var placeId = formatLabel(data.place_id);
        var adresse = formatLabel(data.vicinity);
        var lat = data.geometry.location.lat;
        var lng = data.geometry.location.lng;
        console.log('(\'' +name+'\',\''+rating+'\',\''+photoReference+'\',\''+placeId+'\',\''+adresse+'\',\''+lat+'\',\''+lng+'\'),')
    })
}
/**
 * Supprime le premier et le dernier espace
 * cast 
 */
function formatLabel(label){
    return label.replace(/'/g, 'A');
}

